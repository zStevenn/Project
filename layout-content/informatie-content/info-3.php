        <h1>
            Soorten Afasie
        </h1>
        <p>
            Uitleg over de verschillende soorten afasie. Er zijn geen twee mensen die dezelfde afasie hebben.

            Omdat elke persoon uniek is, verschilcht ook - binnen bepaalde grenzen - de plaats van de functies in de
            hersenen van persoon tot persoon.

            De soort en de ernst van de afasie worden bepaald door:

            <ul>
                <li>de plaats van de hersenbeschadiging</li>
                <li>de grootte van de hersenbeschadiging.</li>
                <li>In de medische wereld zijn veel vormen van afasie beschreven.</li>
            </ul>


            De vier hoofdvormen zijn:
            <ul>
                <li>Amnestische afasie</li>
                <li>Broca afasie</li>
                <li>Globale afasie</li>
                <li>Wernicke-afasie</li>
            </ul>
            Ook kennen we nog Conductie afasie, Auditieve afasie en Visuele afasie.

            Bij de ernstige vormen van afasie komt het voor dat de persoon ”blijft hangen” op een bepaald woord, een
            bepaalde zin of uitdrukking: een nieuw onderwerp wordt besproken maar de persoon blijft het vorige woord of
            onderwerp herhalen. Dit verschijnsel wordt perseveratie genoemd.
        </p>
    </div>
