        <h1>Tip's voor communicatie!</h1>
        <p>
            Bedenk allereerst wat u zelf makkelijk vindt als u in het buitenland bent.
            Het eerste wat u aan een rad sprekende Fransman vraagt is om langzaam te spreken.
            Ook is het gemakkelijk als u van tevoren weet waarover het gesprek zal gaan.
            Als u de weg vraagt en iemand helpt u door snel een tekening te maken of iets aan te wijzen, wordt het voor
            u makkelijker om te begrijpen wat hij bedoelt.
        </p>
        <ul>
            <li>Zorg voor een rustige omgeving. Geluiden van een radio of televisie of ander achtergrondlawaai zijn
                storend. </li>
            <li>Maak eerst oogcontact, en neem de tijd voor een gesprek.</li>
            <li>Spreek zoveel mogelijk in korte eenvoudige zinnen en benadruk de belangrijkste woorden uit de zin
                (trefwoorden) en schrijf deze op.
                Maak eventueel een eenvoudige tekening.
                Dat helpt de persoon met afasie om uw boodschap te begrijpen en te onthouden,
                en bovendien kan hij het geschrevene later gebruiken om aan een ander te vertellen wat er met u
                besproken is. </li>
            <li>Controleer altijd of de boodschap begrepen is en herhaal eventueel het besprokene aan de hand van
                datgene wat u opgeschreven heeft.</li>
            <li>Stimuleer iemand met afasie `te praten met handen en voeten`. Het gaat er niet om hoe hij iets duidelijk
                maakt, als u het maar begrijpt.
                Het helpt vaak als u zelf gebaren maakt of aanwijst waarover u praat.
                U kunt hierbij gebruik maken van <a href="https://webshop.hersenletsel.nl/Gespreksboek">`het
                    gespreksboek` </a>van de Patienten verenging hersenletsel. </li>
            <li>Stel eenvoudige vragen en breng structuur aan in het gesprek. 
                Vraag eerst over wie het gaat, daarna wat er gebeurd is en eventueel waar of wanneer. </li>
        </ul>
        <p>Meer informatie en tips die de communicatie kunnen verbeteren vindt u in de brochure `Richtlijnen voor de communicatie`, 
            die verkrijgbaar is <a href="https://webshop.hersenletsel.nl/Richtlijnen-voor-de-communicatie"> in de webshop van patientenstichting hersenletsel.</a>
        </p>
        <p>Deze tips zijn bedoeld om de communicatie met mensen met afasie beter te laten verlopen.
             Het gebruik maken van de tips is echter geen garantie voor een communicatie op het niveau van voor de afasie.
             Als u er echt niet uitkomt, kunt u beter het onderwerp even laten rusten en er later op terug komen. Misschien lukt het dan wel. </p>
        <p>Geef de communicatie met iemand met afasie nooit op! Het is belangrijk dat u blijft proberen met elkaar te `praten`.
             Communicatie is voor alle mensen van levensbelang; ook voor mensen met afasie! </p>
        <p>Soms kan het handig zijn om bij het praten met elkaar gebruik te maken van hulpmiddelen. 
            Bij een bezoek aan het buitenland nemen we immers ook een reiswoordenboek mee? 
            Foto`s, afbeeldingen, woordenlijstjes, â€˜het gespreksboek` of apps die ontwikkeld zijn om de communicatie te vergemakkelijken kunnen veel verduidelijken.</p>
        <p>Zo kunt u, eventueel samen met uw partner, een `communicatieschrift` maken. 
            Dit is een schrift waarin de mensen uit de omgeving van de persoon met afasie kunnen opschrijven of tekenen wat ze met hem besproken hebben. 
            Hij heeft dan altijd een geheugensteuntje bij de hand. Ook kunnen in dat schrift de al bekende dingen genoteerd worden. 
            Dan hoeft hij deze alleen nog maar op te zoeken en aan te wijzen.</p>
